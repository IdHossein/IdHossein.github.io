onMounted(() => {
  const cards = document.querySelectorAll<HTMLElement>('.skill-card')

  cards.forEach(card => {
    const bar = card.querySelector<HTMLElement>('.skill-bar-fill')
    if (bar) bar.style.width = '0'
  })

  observer = new IntersectionObserver(
    (entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          const bar = entry.target.querySelector<HTMLElement>('.skill-bar-fill')
          const target = bar?.dataset.progress
          if (bar && target) bar.style.width = target
        }
      })
    },
    { threshold: 0.5 },
  )

  cards.forEach(card => observer.observe(card))
})

onUnmounted(() => observer?.disconnect())
